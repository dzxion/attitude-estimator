function [ q1 ] = integral( q0, gyro, dt )

q1(1,:) = q0(1,:) + 0.5 * ( -q0(2,:)*gyro(1,:) -q0(3,:)*gyro(2,:) -q0(4,:)*gyro(3,:) ) * dt;
q1(2,:) = q0(2,:) + 0.5 * ( q0(1,:)*gyro(1,:) +q0(3,:)*gyro(3,:) -q0(4,:)*gyro(2,:) ) * dt;
q1(3,:) = q0(3,:) + 0.5 * ( q0(1,:)*gyro(2,:) -q0(2,:)*gyro(3,:) +q0(4,:)*gyro(1,:) ) * dt;
q1(4,:) = q0(4,:) + 0.5 * ( q0(1,:)*gyro(3,:) +q0(2,:)*gyro(2,:) -q0(3,:)*gyro(1,:) ) * dt;

q1 = q1 / norm(q1);

end